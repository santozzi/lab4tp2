class UserEntity {
  final String id;
  final String email;
  final String password;
  final String name;
  final String role;
  final String avatar;
  final String gender;
  final String country;
  final String city;
  final String state;
  final String phone;

  UserEntity({
    required this.id,
    required this.email,
    required this.password,
    required this.name,
    required this.role,
    required this.avatar,
    required this.gender,
    required this.country,
    required this.city,
    required this.state,
    required this.phone,
  });
}
